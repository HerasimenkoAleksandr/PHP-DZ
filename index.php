Для зручності роботи з кількома проєктами (сайтамми) бажано налаштувати локальний хостінг
-папка з конфігурацією Apache
-за зразком наведеним у конфігурації, створюємо запис для нашого проєкту
-додаємо імена локальних сайтів до локальної служби DNS
=преходимо до C:\Windows\System32\drivers\etc
=відкриваємо на редагування (адмі) файл hosts

    127.0.0.1        php-dz.loc
    ::1              php-dz.loc
    127.0.0.1        www.php-dz.loc
    ::1              www.php-dz.loc

-зупиняємо та запускаємо Apache з XAMPP
