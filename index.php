PHP  
Мова програмування, початково призначена для покращення можливостей
HTML (зміни, інсьрукції, вирази, файли/шаборни)
На сьогодні - повноцінна мова програмування, яка частіше використовується для задач бекенду.

За приблизною аналогією з JS, PHP має подвійне розуміння 
-частина (модуль) веб-сервера, яка продовжує оброблення запитів
-самостійний продукт для програм.

Для встановлення краще за все використати збіпку (налаштовані між собою веб-сервер, PHP, СУБД та інше).
Такими є XAMPP, OpenServer, Danver ...

веб-запит--[Apache(веб-сервер)] сайт1 (index.php) або сайтN (index.php)
веб-сервер приймає запит, розбирає його (виділяє метод, заголовки, передані форми, файли тощо) 
та запускає PHP файл у запитуваному сайті.

Виведення PHP (як модуля) потряпляє не на "екран", а в тіло HTML

-тип мови: інтерпретатор (Repl)
-4GL
-парадигма: процедурна(з підтримкою ООП)
